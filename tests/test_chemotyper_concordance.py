        pred, ref, bit_names, n_failed = _compute_concordance_from_arrays(
            fp_toxprint, clinventory_smiles, clinventory_toxprint_df
        )
        _save_clinv_cache(cache_path, CLINVENTORY_TOXPRINT_ZIP, pred, ref, bit_names, n_failed)
