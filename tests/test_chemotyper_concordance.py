def _compute_concordance_from_arrays(fp_obj, smiles_list, ref_df, row_mask=None):
    """Parse SMILES, run fingerprinter, align to reference DataFrame.

    Parameters
    ----------
    fp_obj      : Fingerprinter with ``bit_names`` and ``fingerprint_batch``
    smiles_list : list[str], length N (same row order as ref_df)
    ref_df      : DataFrame with bit columns; length N
    row_mask    : optional bool array of length N to restrict to a subset

    Returns
    -------
    pred_matrix  : (n_valid, n_bits) int array
    ref_matrix   : (n_valid, n_bits) int array
    bit_names    : list[str]
    n_failed     : int — SMILES that could not be parsed
    """
    bit_names = fp_obj.bit_names

    if row_mask is not None:
        indices = np.where(row_mask)[0]
        smiles_subset = [smiles_list[i] for i in indices]
        ref_subset = ref_df.iloc[indices]
    else:
        smiles_subset = smiles_list
        ref_subset = ref_df

    mols, valid_local_idx = [], []
    for i, smi in enumerate(smiles_subset):
        mol = Chem.MolFromSmiles(str(smi))
        if mol is not None:
            mols.append(mol)
            valid_local_idx.append(i)

    n_failed = len(smiles_subset) - len(mols)
    pred_matrix = fp_obj.fingerprint_batch(mols).astype(int)
    # reindex ensures all expected bits are present even if ChemoTyper omitted
    # zero-valued columns from its export
    ref_matrix = (
        ref_subset
        .reindex(columns=bit_names, fill_value=0)
        .iloc[valid_local_idx]
        .to_numpy(dtype=int)
    )
    return pred_matrix, ref_matrix, bit_names, n_failed


def _compute_extra_metrics(pred: "np.ndarray", ref: "np.ndarray") -> dict:
    """Return per-bit MCC, avg precision, balanced accuracy and ROC-AUC, plus macro averages.

    Bits where the reference is all-zero or all-one are skipped (NaN).
    ROC-AUC equals balanced accuracy for a hard (threshold-free) classifier.
    """
    n_bits = pred.shape[1]
    mcc = np.full(n_bits, np.nan)
    avg_prec = np.full(n_bits, np.nan)
    bal_acc = np.full(n_bits, np.nan)
    roc_auc = np.full(n_bits, np.nan)
    for i in range(n_bits):
        y_true, y_pred = ref[:, i], pred[:, i]
        pos = int(y_true.sum())
        if pos == 0 or pos == len(y_true):
            continue
        tp = int(((y_pred == 1) & (y_true == 1)).sum())
        tn = int(((y_pred == 0) & (y_true == 0)).sum())
        fp_ = int(((y_pred == 1) & (y_true == 0)).sum())
        fn_ = int(((y_pred == 0) & (y_true == 1)).sum())
        denom = ((tp + fp_) * (tp + fn_) * (tn + fp_) * (tn + fn_)) ** 0.5
        mcc[i] = (tp * tn - fp_ * fn_) / denom if denom > 0 else 0.0
        sensitivity = tp / (tp + fn_) if (tp + fn_) > 0 else 0.0
        specificity = tn / (tn + fp_) if (tn + fp_) > 0 else 0.0
        bal_acc[i] = (sensitivity + specificity) / 2.0
        roc_auc[i] = bal_acc[i]
        prec_1 = tp / (tp + fp_) if (tp + fp_) > 0 else 0.0
        avg_prec[i] = prec_1 * sensitivity
    return {
        "mcc": mcc,
        "avg_precision": avg_prec,
        "balanced_acc": bal_acc,
        "roc_auc": roc_auc,
        "macro_mcc": float(np.nanmean(mcc)),
        "macro_avg_precision": float(np.nanmean(avg_prec)),
        "macro_balanced_acc": float(np.nanmean(bal_acc)),
        "macro_roc_auc": float(np.nanmean(roc_auc)),
    }


